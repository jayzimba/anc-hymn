// Helper function to get hymns for a specific language
export const getHymnsForLanguage = (language) => {
  return hymnData[language] || [];
};

// Helper function to get a specific hymn by ID and language
export const getHymnById = (language, hymnId) => {
  const hymns = getHymnsForLanguage(language);
  return hymns.find(hymn => hymn.id === hymnId);
};

// Helper function to search hymns by title or number
export const searchHymns = (language, query) => {
  const hymns = getHymnsForLanguage(language);
  const lowercaseQuery = query.toLowerCase();
  return hymns.filter(hymn => 
    hymn.title.toLowerCase().includes(lowercaseQuery) ||
    hymn.number.includes(query) ||
    (hymn.content && hymn.content.toLowerCase().includes(lowercaseQuery))
  );
};

// Helper function to get hymn content
export const getHymnContent = (language, hymnId) => {
  const hymn = getHymnById(language, hymnId);
  return hymn?.content || '';
};

// Helper function to get hymn content by number
export const getHymnContentByNumber = (language, number) => {
  const hymns = getHymnsForLanguage(language);
  const hymn = hymns.find(h => h.number === number);
  return hymn?.content || '';
}; 